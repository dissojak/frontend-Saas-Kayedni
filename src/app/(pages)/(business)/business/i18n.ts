export type BusinessManageKey = keyof typeof BUSINESS_MANAGE_DICTIONARY.en;

function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) {
    return template;
  }

  return template.replaceAll(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

export function businessManageT(
  locale: LocaleCode,
  key: BusinessManageKey,
  params?: Record<string, string | number>,
): string {
  const dictionary = BUSINESS_MANAGE_DICTIONARY[locale] ?? BUSINESS_MANAGE_DICTIONARY.en;
  const value = dictionary[key] ?? BUSINESS_MANAGE_DICTIONARY.en[key];
  return interpolate(value, params);
}

const STATUS_KEY_MAP: Record<string, BusinessManageKey> = {
  ACTIVE: 'status_active',
  PENDING: 'status_pending_review',
  SUSPENDED: 'status_suspended',
  DRAFT: 'status_draft',
  INACTIVE: 'status_inactive',
};

export function businessManageStatusT(locale: LocaleCode, status?: string | null): string {
  const normalized = status?.toUpperCase() ?? '';
  return businessManageT(locale, STATUS_KEY_MAP[normalized] ?? 'status_unknown');
}

